INSERT INTO player_informations (
    playerId,
    jerseyNumber,
    tShirtSize,
    lowerSize,
    hasCricheroesProfile,
    isPaidPlayer,
    pricePerMatch,
    willJoinAnyOwner
)
SELECT
    playerId,
    CASE WHEN RAND() < 0.8 THEN FLOOR(1 + RAND() * 99) END AS jerseyNumber,  -- 80% have numbers
    CASE 
        WHEN RAND() < 0.8 THEN ELT(FLOOR(1 + RAND() * 6), 'XS','S','M','L','XL','XXL')
    END AS tShirtSize,  -- 80% have size
    CASE 
        WHEN RAND() < 0.8 THEN ELT(FLOOR(1 + RAND() * 6), 'XS','S','M','L','XL','XXL')
    END AS lowerSize,  -- 80% have size
    RAND() > 0.4 AS hasCricheroesProfile,  -- 60% true
    RAND() > 0.6 AS isPaidPlayer,  -- 40% true
    CASE 
        WHEN RAND() > 0.6 THEN ROUND(RAND() * 1000, 2)  -- Only for paid players
    END AS pricePerMatch,
    RAND() > 0.3 AS willJoinAnyOwner  -- 70% true
FROM players;