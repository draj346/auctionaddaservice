INSERT IGNORE INTO players (
    name, 
    mobile, 
    email, 
    isSubmitted, 
    isApproved, 
    state, 
    district, 
    isVerified, 
    isActive, 
    isNonPlayer
)
SELECT
    CONCAT(
        SUBSTRING('ABCDEFGHIJKLMNOPQRSTUVWXYZ', FLOOR(1 + RAND() * 26), 1),
        SUBSTRING(MD5(RAND()), 1, FLOOR(8 + RAND() * 8))
    ) AS name,
    CONCAT('8', LPAD(100000000 + n, 9, '0')) AS mobile,
    IF(RAND() < 0.7, CONCAT('user', n, '@example.com'), NULL) AS email,
    RAND() > 0.5 AS isSubmitted,
    RAND() > 0.5 AS isApproved,
    sd.state,
    sd.district,
    CASE
        WHEN RAND() < 0.2 THEN NULL
        WHEN RAND() < 0.5 THEN TRUE
        ELSE FALSE
    END AS isVerified,
    RAND() > 0.2 AS isActive,
    RAND() > 0.9 AS isNonPlayer
FROM (
    SELECT 
        a.N + b.N * 10 + c.N * 100 + d.N * 1000 + e.N * 10000 AS n
    FROM 
        (SELECT 0 AS N UNION ALL SELECT 1 UNION ALL SELECT 2 UNION ALL SELECT 3 UNION ALL SELECT 4 UNION ALL SELECT 5 UNION ALL SELECT 6 UNION ALL SELECT 7 UNION ALL SELECT 8 UNION ALL SELECT 9) a,
        (SELECT 0 AS N UNION ALL SELECT 1 UNION ALL SELECT 2 UNION ALL SELECT 3 UNION ALL SELECT 4 UNION ALL SELECT 5 UNION ALL SELECT 6 UNION ALL SELECT 7 UNION ALL SELECT 8 UNION ALL SELECT 9) b,
        (SELECT 0 AS N UNION ALL SELECT 1 UNION ALL SELECT 2 UNION ALL SELECT 3 UNION ALL SELECT 4 UNION ALL SELECT 5 UNION ALL SELECT 6 UNION ALL SELECT 7 UNION ALL SELECT 8 UNION ALL SELECT 9) c,
        (SELECT 0 AS N UNION ALL SELECT 1 UNION ALL SELECT 2 UNION ALL SELECT 3 UNION ALL SELECT 4 UNION ALL SELECT 5 UNION ALL SELECT 6 UNION ALL SELECT 7 UNION ALL SELECT 8 UNION ALL SELECT 9) d,
        (SELECT 0 AS N UNION ALL SELECT 1 UNION ALL SELECT 2 UNION ALL SELECT 3 UNION ALL SELECT 4 UNION ALL SELECT 5 UNION ALL SELECT 6 UNION ALL SELECT 7 UNION ALL SELECT 8 UNION ALL SELECT 9) e
    WHERE 
        a.N + b.N * 10 + c.N * 100 + d.N * 1000 + e.N * 10000 < 100000
) AS numbers
JOIN (
    SELECT 
        state,
        district,
        RAND() AS rand_order
    FROM state_districts
) sd
ORDER BY sd.rand_order
LIMIT 100000;


